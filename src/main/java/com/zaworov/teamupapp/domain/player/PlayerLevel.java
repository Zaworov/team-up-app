package com.zaworov.teamupapp.domain.player;

public enum PlayerLevel {
D, DD, C, CC, B, BB, A, AA, AAA
//    D - nowy (nigdy wcześniej nie grał w siatkówkę)
//    DD - kilka razy grał w siatkówkę ale ciągle uczy się podstaw
//    C - rekreacyjny
//    CC - rekreacyjny, znający podstawy gry i chcący nauczyć się więcej
//    B - zaawansowany początkujący
//    BB - średnio zaawansowany (SL3 2liga)
//    A - zaawansowany (SL3 1 Liga)
//    AA - gracze lig amatorskich ( liga PALPS)
//    AAA/Open - najwyższy poziom wtajemniczenia 🙂
//    Poziomy gierek:
//    D do CC - spotkania gdzie poziom graczy jest bardzo zróżnicowany ale nikt nie wybiega ponad poziom CC
//    B - To nie poziom dla początkujących. Poziom dla graczy którzy prezentują przyzwoitą kontrolę nad piłką i całkiem dobry atak. Błędy mogą być popełniane jednak nie tak często jak na poziomie D do CC. Większość graczy to sportowcy rozwijający swoje umiejętności lub starsi gracze. Przypadkowe odbicia nie powinny mieć miejsca ale nie jest to poziom dla graczy którzy atakują wszystko w boisko zza linii 3 metra.
//            BB - przeważnie zawodnicy którzy mają doświadczenie w jakiejś drużynie. Większość graczy ze wzrostem w okolicy 180 cm prezentujących dobrą moc i szybkość ataku, ale nie koniecznie pełną wiedzę na temat technik gry. Znajomość poruszania się po boisku w czasie obrony i wyprowadzania ataku na tym poziomie powinny być znacznie wyższe niż poziom początkujący.
//            A - To zaawansowany poziom siatkówki, mocne ataki, blok przy każdym z ataków, niżsi gracze tylko na pozycji libero lub rozgrywającego. Zagrywka głownie z wyskoku lub celowana.
//    AA - Poziom dla graczy ligowych. Każdy z graczy prezentuje wysoki poziom na swojej pozycji, gra przebiega głównie w ustawieniu na jednego rozgrywającego, ataki z 2 linii ze środka i skrzydła, gra kombinacyjna. Zagrywka mocna z wyskoku. Większość graczy to gracze AA czyli 3 liga lub liga PALPS.
//    AAA - poziom po prostu dla najlepszych z najlepszych 🙂
}
